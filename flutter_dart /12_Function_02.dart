  addition(num1, num2){
    return num1 + num2;
  }


  subtraction(num1, num2){
  int B = num1 - num2;
  return B;
  }

  mutiplication(num1, num2){
  var C = num1 * num2;
  return C;
  }

  division(num1, num2){
  double D = num1 / num2;
  return D;
  }
